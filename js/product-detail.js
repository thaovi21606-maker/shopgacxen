// GHI ĐÈ LẠI HÀM SUBMIT REVIEW ĐỂ THÊM AVATAR CHO BÌNH LUẬN MỚI
window.submitReview = function(event) {
    if (event) event.preventDefault(); 
    if (!window.requireLogin()) return false; 

    const nameInput = document.getElementById("review-name");
    const starSelect = document.getElementById("review-star");
    const textInput = document.getElementById("review-text");

    if(!nameInput || !starSelect || !textInput) return false;

    const name = nameInput.value.trim();
    // Giá trị nhận được lúc này chỉ còn là các ký tự sao "★★★★★"
    const star = starSelect.value;
    const text = textInput.value.trim();

    if (name === "" || text === "") {
        alert("❌ Bạn chưa nhập đủ nội dung đánh giá!"); 
        return false;
    }

    const today = new Date();
    const day = today.getDate().toString().padStart(2, "0");
    const month = (today.getMonth() + 1).toString().padStart(2, "0");
    const year = today.getFullYear();
    const currentDate = `${day}/${month}/${year}`;
    
    // Lấy avatar của người dùng đăng nhập hiện tại (nếu có), không có dùng mặc định
    let userStr = localStorage.getItem('gacxen_currentUser');
    let avatarSrc = userStr && JSON.parse(userStr).avatar ? JSON.parse(userStr).avatar : 'img/tk.png';

    const reviewHTML = `
    <div class="review-item" style="border-top: 1px solid #eee; padding: 20px 0;">
        <div class="review-top" style="display: flex; justify-content: space-between; align-items: flex-start; margin-bottom: 10px;">
            <div style="display: flex; align-items: center; gap: 15px;">
                <img src="${avatarSrc}" alt="Avatar" style="width: 45px; height: 45px; border-radius: 50%; object-fit: cover; border: 1px solid #ddd;">
                <div>
                    <strong style="font-size: 16px; color: #333;">${name}</strong>
                    <div class="review-date" style="font-size: 13px; color: #999; margin-top: 4px;">${currentDate}</div>
                </div>
            </div>
            <span class="review-star" style="color: #f1c40f; font-weight: bold; font-size: 18px; letter-spacing: 2px;">${star}</span>
        </div>
        <p class="review-content" style="color: #555; line-height: 1.7; margin: 0; padding-left: 60px;">${text}</p>
    </div>
    `;

    // Chèn bình luận mới nhất lên đầu danh sách
    document.getElementById("review-list").insertAdjacentHTML('afterbegin', reviewHTML);

    alert("✅ Đánh giá của bạn đã được gửi thành công! Cảm ơn bạn.");
    textInput.value = "";
    
    return false;
};

// CÁC HÀM XỬ LÝ KHÁC
window.switchTab = function(event, tabId) {
    document.querySelectorAll('.tab-content').forEach(content => {
        content.style.display = 'none';
        content.classList.remove('active'); 
    });
    document.querySelectorAll('.tab-btn').forEach(btn => {
        btn.classList.remove('active');
        btn.style.borderBottom = 'none'; 
    });
    const targetTab = document.getElementById(tabId);
    if (targetTab) {
        targetTab.style.display = 'block';
        targetTab.classList.add('active');
    }
    event.currentTarget.classList.add('active');
};

window.changeQty = function(num) {
    let input = document.getElementById('soluong');
    if (input) {
        let val = parseInt(input.value) + num;
        if (val >= 1) {
            input.value = val;
        }
    }
};

window.toggleFavorite = function(productId) {
    if (!window.requireLogin()) return;

    let userStr = localStorage.getItem('gacxen_currentUser');
    let currentUserEmail = userStr ? JSON.parse(userStr).email : 'guest';
    
    let favKey = 'gacxen_favorites_' + currentUserEmail;
    let favs = JSON.parse(localStorage.getItem(favKey)) || [];
    const btn = document.getElementById('fav-btn-' + productId);
    const icon = btn.querySelector('i');

    if (favs.includes(productId)) {
        favs = favs.filter(id => id !== productId);
        icon.classList.remove('fas');
        icon.classList.add('far');
        icon.style.color = '#999';
        btn.style.borderColor = '#ddd';
    } else {
        favs.push(productId);
        icon.classList.remove('far');
        icon.classList.add('fas');
        icon.style.color = '#ff4d6d';
        btn.style.borderColor = '#ff4d6d';
    }
    
    localStorage.setItem(favKey, JSON.stringify(favs));
    
    if (typeof window.updateGlobalCounters === 'function') {
        window.updateGlobalCounters();
    } else {
        updateFavCount(); 
    }
};

function updateFavCount() {
    let userStr = localStorage.getItem('gacxen_currentUser');
    let currentUserEmail = userStr ? JSON.parse(userStr).email : 'guest';
    let favKey = 'gacxen_favorites_' + currentUserEmail;
    let favs = JSON.parse(localStorage.getItem(favKey)) || [];
    
    let favCountEl = document.getElementById('favCount');
    if (favCountEl) {
        favCountEl.innerText = favs.length;
        favCountEl.style.transform = 'scale(1.3)';
        setTimeout(() => { favCountEl.style.transform = 'scale(1)'; }, 200);
    }
}
